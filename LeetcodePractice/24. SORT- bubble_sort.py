"""
    Bubble sort using a LinkedList
"""

class Node:
    def __init__(self, value):
        self.value = value
        self.next = None
        

class LinkedList:
    def __init__(self, value):
        new_node = Node(value)
        self.head = new_node
        self.tail = new_node
        self.length = 1

    def print_list(self):
        temp = self.head
        while temp is not None:
            print(temp.value)
            temp = temp.next
        
    def append(self, value):
        new_node = Node(value)
        if self.head is None:
            self.head = new_node
            self.tail = new_node
        else:
            self.tail.next = new_node
            self.tail = new_node
        self.length += 1

    def bubble_sort(self):
        """
            sort list using Linked List
            Steps:
                1. If length is < 2, return
                2. Compare 2 items, and swap if the latter is bigger
                3. Run the length of the list times
        """
        if self.length < 2:
            return
        
        for _ in range(self.length):
            current = self.head
            after = self.head.next
            while current is not None and after is not None:
                if current.value > after.value:
                    current.value, after.value = after.value, current.value
                current = after
                after = after.next



my_linked_list = LinkedList(4)
my_linked_list.append(2)
my_linked_list.append(6)
my_linked_list.append(5)
my_linked_list.append(1)
my_linked_list.append(3)

print("Linked List Before Sort:")
my_linked_list.print_list()

my_linked_list.bubble_sort()

print("\nSorted Linked List:")
my_linked_list.print_list()



