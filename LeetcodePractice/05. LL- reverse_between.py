class Node:
    def __init__(self, value):
        self.value = value
        self.next = None
        
class LinkedList:
    def __init__(self, value):
        new_node = Node(value)
        self.head = new_node
        self.tail = new_node
        self.length = 1
        
    def print_list(self):
        temp = self.head
        while temp is not None:
            print(temp.value)
            temp = temp.next   
            
    def make_empty(self):
        self.head = None
        self.tail = None
        self.length = 0

    def append(self, value):
        new_node = Node(value)
        if self.length == 0:
            self.head = new_node
            self.tail = new_node
        else:
            self.tail.next = new_node
            self.tail = new_node
        self.length += 1
        return True
    
    def get(self, index):
        if index < 0 or index >= self.length:
            return None
        temp = self.head
        for _ in range(index):
            temp = temp.next
        return temp

    def reverse_between(self, start:int, end:int):
        """
        reverse nodes from start to end parameter

        Args:
            start (int): start of where you want to reverse
            end (int): end of where you want to reverse
        """
        # logic: 
            # prev points to after, because after will reach the end first
            # current and after both progress by 1, prev doesn't move because it's the head and will just point to the last thing
            # we are actually just swapping two things at a time
        if self.head is None:
            return None

        # set dummy node to keep track of head
        dummy = Node(0)
        dummy.next = self.head
        prev = dummy

        # move previous node to before start
        for _ in range(start):
            prev = prev.next
        
        current = prev.next 
        # exchange start to end
        for _ in range(end - start):
            after = current.next
            current.next = after.next
            # always get this one wrong, can't set it to current.next because current will move
            after.next = prev.next
            prev.next = after
                
        # reset head
        self.head = dummy.next



linked_list = LinkedList(1)
linked_list.append(2)
linked_list.append(3)
linked_list.append(4)
linked_list.append(5)

print("Original linked list: ")
linked_list.print_list()

# Reverse a sublist within the linked list
linked_list.reverse_between(2, 4)
print("Reversed sublist (2, 4): ")
linked_list.print_list()

# Reverse another sublist within the linked list
linked_list.reverse_between(0, 4)
print("Reversed entire linked list: ")
linked_list.print_list()

# Reverse a sublist of length 1 within the linked list
linked_list.reverse_between(3, 3)
print("Reversed sublist of length 1 (3, 3): ")
linked_list.print_list()

# Reverse an empty linked list
empty_list = LinkedList(1)
empty_list.make_empty()
empty_list.reverse_between(0, 0)
print("Reversed empty linked list: ")
empty_list.print_list()


